/* Vamos entender Variáveis
Variáveis são espaços na memória onde podemos armazenar valores.

No JavaScript temos 3 palavras-chaves para declarar variáveis: var, let e const.
*/

let x = 10  //  aqui é 10

{
        let x = 2; // aqui é 2
}

